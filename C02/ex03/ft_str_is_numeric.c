#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	int i;
	int no_digits;

	i = 0;
	no_digits = 0;
	if (str[i] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if (str[i] < 47 || str[i] > 58)
		{
			no_digits++;
		}
		i++;
	}
	if (no_digits > 0)
		return (0);
	else
		return (1);
}
