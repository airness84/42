#include <unistd.h>

int		ft_str_is_printable(char *str)
{
	int i;
	int no_printables;

	i = 0;
	no_printables = 0;
	if (str[i] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if (str[i] < 32 || str[i] == 127)
		{
			no_printables++;
		}
		i++;
	}
	if (no_printables > 0)
		return (0);
	else
		return (1);
}
