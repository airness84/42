int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb > 2147395600 || nb <= 0)
		return (0);
	while (i * i < nb)
		i++;
	if (i * i != nb)
		return (0);
	else
		return (i);
}
