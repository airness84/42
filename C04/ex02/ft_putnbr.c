#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		div;
	char	c;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb *= -1;
			write(1, "-", 1);
		}
		div = nb / 10;
		if (div > 0)
			ft_putnbr(div);
		c = nb % 10 + '0';
		write(1, &c, 1);
	}
}
