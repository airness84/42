#include <unistd.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int				i;
	unsigned int	j;
	int				dest_len;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	dest_len = i;
	while (j < nb && src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
