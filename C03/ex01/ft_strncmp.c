#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, int n)
{
	int i;

	i = 0;
	n--;
	while ((n > 0) && (s1[i] != '\0' && s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
			break ;
		n--;
		i++;
	}
	return (s1[i] - s2[i]);
	if (n == 0)
		return (0);
}
