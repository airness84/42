#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int result;

	i = 0;
	result = 0;
	while ((s1[i] == s2[i]) && (s1[i] != '\0' || s2[i] != '\0'))
	{
		i++;
	}
	if (s1[i] != '\0' || s2[i] != '\0')
		result = s1[i] - s2[i];
	if (s1[i] == s2[i])
		result = 0;
	return (result);
}
