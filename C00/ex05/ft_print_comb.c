#include <unistd.h>

void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a = 48;
	b = 49;
	c = 50;
	while (a <= 55)
	{
		while (c <= 57)
		{
			write(1, &a, 1);
			write(1, &b, 1);
			write(1, &c, 1);
			c++;
			write(1, " ", 1);
			write(1, ",", 1);
		}
		a++;
		b = a + 1;
		c = b + 1;
	}
}
